                $("#repeatpassword").focus();
                return;
            }




            if(password == repeatpassword){
                $.ajax('functions/userregister.php', {
                    type: 'POST',  // http method
                    data: { 'fname': fname,'email': email, 'password': password ,'mobile':mobile },  // data to submit
                    success: function (data, status, xhr) {
    

               if(data == 'present' ){
                    alert("This User Already Exist !");
                    return false;

                }
            
                        if(data != "0"){
                            alert("New user created successfully");
                            $(location).attr('href', 'parent/parentdashboard.php')
                        }
                        else{
                            alert("Something went wrong. Please try again in sometime.");
                        }
                    },
                    error: function (jqXhr, textStatus, errorMessage) {
                            alert('Error' + errorMessage);
                    }
                });
            }
            else{
                alert("Password does not match");
                $("#password").val("");
                $("#repeatpassword").val("");
                $("#password").focus();
            }
        });


